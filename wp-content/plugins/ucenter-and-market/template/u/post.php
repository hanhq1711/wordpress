<div class="area">
    <div class="page-wrapper">
        <div class="dashboard-main">
            <div class="dashboard-header">
				<p class="sub-title">您已发布<span><?php echo $posts_count; ?></span>篇文章作品<a <?php echo is_user_logged_in() ? 'href="'.add_query_arg(array('tab'=>'post','action'=>'new'), get_author_posts_url($current_user->ID)).'" ' : 'href="javascript:" class="user-login"' ; ?>><span class="new-post-btn">写文章</span></a></p>
				<!--p class="tip">提示：删除后不可恢复，请谨慎操作。</p-->
				<!-- Page global message -->
				<?php if($message) echo '<div class="alert alert-success">'.$message.'</div>'; ?>
			</div>
            <div class="dashboard-wrapper select-posts">
				<article class="panel panel-default archive" role="main">
<?php 
	global $wp_query;
	$args = is_user_logged_in() ? array_merge( $wp_query->query_vars, array( 'post_status' => array( 'publish', 'pending', 'draft' ) ) ) : $wp_query->query_vars;
	query_posts( $args );
	while ( have_posts() ) : the_post();
	include(UM_DIR.'template/content-archive.php');
	endwhile; // end of the loop. 
	um_paginate();
	wp_reset_query();
?>		
				</article>
            </div>
